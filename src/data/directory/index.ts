export const directoryCategories = categories;

export function getDirectoryCategoryBySlug(slug: string): DirectoryCategory | undefined {
  return categories.find((c) => c.slug === slug);
}

export function getDirectoryListingsByCategory(categorySlug: string): DirectoryListing[] {
  return listings.filter((l) => l.subcategory === categorySlug);
}

export function getDirectoryParentCategory(slug: string): DirectoryParentCategory | undefined {
  return parentCategories.find((p) => p.slug === slug);
}

export function getDirectoryListingsByRegion(region: string): DirectoryListing[] {
  return listings.filter((l) => l.regions.some((r) => r.toLowerCase() === region.toLowerCase()));
}

export function searchDirectory(query: string): DirectoryListing[] {
  const q = query.toLowerCase();
  return listings.filter(
    (l) =>
      l.name.toLowerCase().includes(q) ||
      l.description.toLowerCase().includes(q) ||
      l.services.some((s) => s.toLowerCase().includes(q)) ||
      l.regions.some((r) => r.toLowerCase().includes(q))
  );
}
